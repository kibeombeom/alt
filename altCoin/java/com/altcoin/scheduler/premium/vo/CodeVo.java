package com.altcoin.scheduler.premium.vo;

import java.util.Date;

public class CodeVo {
	
	private int cdNo;
	private String cdCategory;
	private String cdKey;
	private String cdVal;
	private Date createTime;
	private int isUser;
	
	public int getCdNo() {
		return cdNo;
	}
	public void setCdNo(int cdNo) {
		this.cdNo = cdNo;
	}
	public String getCdCategory() {
		return cdCategory;
	}
	public void setCdCategory(String cdCategory) {
		this.cdCategory = cdCategory;
	}
	public String getCdKey() {
		return cdKey;
	}
	public void setCdKey(String cdKey) {
		this.cdKey = cdKey;
	}
	public String getCdVal() {
		return cdVal;
	}
	public void setCdVal(String cdVal) {
		this.cdVal = cdVal;
	}
	public Date getCreateTime() {
		return createTime;
	}
	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}
	public int getIsUser() {
		return isUser;
	}
	public void setIsUser(int isUser) {
		this.isUser = isUser;
	}
	
	
	
	
}

package com.altcoin.scheduler.premium.vo;

public class ExchangeRateVo {
	
	private int exchangeNo;
	private int countryCd;
	private int currencyCd;
	private float exchangeValue;
	private int isUse;
	
	
	public int getExchangeNo() {
		return exchangeNo;
	}
	public void setExchangeNo(int exchangeNo) {
		this.exchangeNo = exchangeNo;
	}
	public int getCountryCd() {
		return countryCd;
	}
	public void setCountryCd(int countryCd) {
		this.countryCd = countryCd;
	}
	public int getCurrencyCd() {
		return currencyCd;
	}
	public void setCurrencyCd(int currencyCd) {
		this.currencyCd = currencyCd;
	}
	public float getExchangeValue() {
		return exchangeValue;
	}
	public void setExchangeValue(float exchangeValue) {
		this.exchangeValue = exchangeValue;
	}
	public int getIsUse() {
		return isUse;
	}
	public void setIsUse(int isUse) {
		this.isUse = isUse;
	}
	
	
	
	
	
	
}
